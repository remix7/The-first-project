package text;

import java.util.Scanner;

public class Acb {
	public static void main(String[] args) {
		System.out.print("请输入一个数：");
		Scanner s = new Scanner(System.in);
		int a = s.nextInt();
		switch (a) {
		case 1:
			System.out.println(1);
			break;
		case 2:
			System.out.println(2);
			break;
		case 3:
			System.out.println(3);
			break;

		default:
			break;
		}
		
		
	}
}
